#include <hic.h>
#include "type.h"

/**************************************************************************
* 函数名称：delay_ms
* 功能描述：毫秒级延时函数
* 输入参数：ms   延时毫秒数，输入范围0-65535
* 返回参数：无
* 函数作者：
* 完成日期：
* 修订历史：
* 修订日期：
**************************************************************************/
void delay_ms(uint16_t ms)
{
	uint16_t i;

	for(;ms>0;ms--)
	{
		for(i = 0;i<500;i++);
	}
}

/**************************************************************************
* 函数名称：delay_us
* 功能描述：微秒级延时函数
* 输入参数：us   延时微妙数，输入范围0-65535
* 返回参数：无
* 函数作者：
* 完成日期：
* 修订历史：
* 修订日期：
**************************************************************************/
void delay_us(uint16_t us)
{
	uint16_t i;
	
	for (; us>0; us--) 
	{
		for(i = 0;i<20;i++);
	}
}

